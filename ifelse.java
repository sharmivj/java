public class ifelse
{
    public static void main(String args[])
    {
        int mark=100;
        if(mark>90)
        {
            System.out.println("grade:o");
        }
        else if(mark>80)
        {
            System.out.println("grade:a");
        }
        else if(mark>70)
        {
            System.out.println("grade:b");
        }
        }
}