public class swapp2
{
    public static void main(String args[])
    {
        int temp,x=10,y=20;
        System.out.println("before swapping:");
        System.out.println(x);
        System.out.println(y);
        System.out.println("After swapping:");
        temp=x;
        x=y;
        y=temp;
        System.out.println(x);
        System.out.println(y);
        
    
    }
}